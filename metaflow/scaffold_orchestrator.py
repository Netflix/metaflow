def _classname(name: str) -> str:
    """Convert snake_case or kebab-case to PascalCase."""
    return "".join(part.capitalize() for part in name.replace("-", "_").split("_"))


def _write(path: str, content: str) -> None:
    os.makedirs(os.path.dirname(os.path.abspath(path)), exist_ok=True)
    with open(path, "w") as f:
        f.write(content)
    print(f"  wrote {path}")


def scaffold(name: str, output_dir: str = ".") -> None:
    cls = _classname(name)

    files = {
        f"{name}_deployer.py": _DEPLOYER_TEMPLATE.format(name=name, classname=cls),
        f"{name}_objects.py": _OBJECTS_TEMPLATE.format(name=name, classname=cls),
        f"{name}_cli.py": _CLI_TEMPLATE.format(name=name, classname=cls),
        f"ux-tests-{name}.yml": _GHA_TEMPLATE.format(name=name, classname=cls),
    }

    print(f"Scaffolding orchestrator: {name!r} (class prefix: {cls})")
    for filename, content in files.items():
        _write(os.path.join(output_dir, filename), content)

    print()
    print("Next steps:")
    print(f"  1. Implement TODO sections in {name}_deployer.py and {name}_objects.py")
    print(f"  2. Register the CLI group in metaflow/plugins/__init__.py")
    print(f"  3. Add a backend entry to test/ux/ux_test_config.yaml")
    print(f"  4. Run the compliance tests:")
    print(
        f"     python -m pytest test/ux/core/test_compliance.py "
        f"--only-backend {name} -v"
    )
    print()
    print("Contract checklist (things every orchestrator gets wrong):")
    print("  [ ] run_params is a list (not a tuple) in trigger()")
    print("  [ ] --branch forwarded to every step subprocess")
    print("  [ ] METAFLOW_FLOW_CONFIG_VALUE injected into container env")
    print("  [ ] retry_count derived from scheduler native attempt number")
    print("  [ ] All REQUIRED_ENV_VARS populated at step launch time")


def main():
    if len(sys.argv) < 2:
        print(__doc__)
        print("Usage: python -m metaflow.scaffold_orchestrator <name>")
        sys.exit(1)

    name = sys.argv[1].replace("-", "_")
    output_dir = sys.argv[2] if len(sys.argv) > 2 else "."
    scaffold(name, output_dir)


if __name__ == "__main__":
    main()
