"""
Metaflow orchestrator scaffolding tool.

Generates a complete orchestrator skeleton with all contract requirements
pre-filled and annotated.  This makes it hard to forget the requirements
that every orchestrator gets wrong the first time.

Usage:
    python -m metaflow.scaffold_orchestrator my_scheduler

This creates:
    ./my_scheduler_deployer.py   — DeployerImpl subclass
    ./my_scheduler_objects.py    — DeployedFlow / TriggeredRun subclass
    ./my_scheduler_cli.py        — CLI entry-point group
    ./ux-tests-my_scheduler.yml  — GitHub Actions workflow skeleton

The generated files compile without modification.  Replace every
TODO / FIXME comment with real implementation code.
"""

